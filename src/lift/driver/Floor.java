package lift.driver;

/** Class which represents a floor */

public class Floor {
int number;

public Floor(int x){
	number=x;
}

/**
 * @return the number
 */
public int getNumber() {
	return number;
}

/**
 * @param number the number to set
 */
void setNumber(int number) {
	this.number = number;
}

}
